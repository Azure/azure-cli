from knack.help_files import helps

# pylint: disable=line-too-long, too-many-lines

helps['network nat-gateway'] = """
    type: group
    short-summary: Manage NAT gateways.
"""

helps['network nat-gateway create'] = """
    type: command
    short-summary: Create NAT Gateways
    parameters:
      - name: --public-ip-addresses
        short-summary: Space seperated list of Public Ip Addresses.
      - name: --public-ip-prefixes
        short-summary: Space seperated list of Public Ip Prefixes.
      - name: --idle-timeout
        short-summary: Idle Timeout in minutes.
    examples:
      - name: Create Nat Gateway
        text: az network nat-gateway create --resource-group MyResourceGroup --name MyNatGateway --location MyLocation --public-ip-addresses  MyPublicIp --public-ip-prefixes  MyPublicIpPrefix --idle-timeout 4
"""

helps['network nat-gateway delete'] = """
    type: command
    short-summary: Delete NAT gateways.
    examples:
      - name: Delete Nat Gateway
        text: az network nat-gateway delete --resource-group MyResourceGroup --name MyNatGateway
"""

helps['network nat-gateway list'] = """
    type: command
    short-summary: List NAT gateways.
    examples:
      - name: Delete an outbound-rule. (autogenerated)
        text: az network nat-gateway list -g MyResourceGroup
"""

